package momenthere.login;

public class Message {
	
	public String name;
	public String message;
	public String location;
	public String time;
	
	public Message(String name, String message, String location, String time) {
		super();
		this.name = name;
		this.message = message;
		this.location = location;
		this.time = time;
	}
}
